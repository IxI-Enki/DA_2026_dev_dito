"""Generate evaluation reports (MD, JSON, HTML).

Executive Summary, Metriken-Tabellen (Kap. 6), Vergleichstabellen FF1/FF3/J2/J4/J6.
Used by RAG Evaluator Agent - Report Generation skill.

Usage::
    python -m evaluation.ragas_agents.scripts.generate_report --results-dir <evaluation_results_dir> --stats <statistical_analysis.json>
"""

from __future__ import annotations

import argparse
import json
import logging
import sys
from pathlib import Path

import yaml

logging.basicConfig(level=logging.INFO, format="%(levelname)s: %(message)s")
logger = logging.getLogger(__name__)

SCRIPT_DIR = Path(__file__).resolve().parent
RAGAS_AGENTS_ROOT = SCRIPT_DIR.parent
REPO_ROOT = RAGAS_AGENTS_ROOT.parent.parent


def load_config(config_path: Path | None = None) -> dict:
    if config_path is None:
        config_path = RAGAS_AGENTS_ROOT / "config" / "ragas_config.yaml"
    if not config_path.exists():
        return {}
    with open(config_path, encoding="utf-8") as f:
        return yaml.safe_load(f) or {}


def build_markdown_report(results_dir: Path, stats_path: Path | None) -> str:
    """Build Markdown report from evaluation results and optional stats."""
    lines = [
        "# RAGAS Evaluation Report",
        "",
        "## Executive Summary",
        "",
        "This report summarizes the RAG evaluation results for the LeoWiki RAG pipeline (Diploma thesis FF1, FF3, J2, J4, J6).",
        "",
    ]
    if stats_path and stats_path.exists():
        with open(stats_path, encoding="utf-8") as f:
            stats = json.load(f)
        lines.append("### Aggregated Tables")
        lines.append("")
        for name, row in stats.get("tables", {}).items():
            lines.append(f"#### {name}")
            lines.append("")
            for k, v in row.items():
                if not k.startswith("_"):
                    lines.append(f"- **{k}**: {v}")
            lines.append("")
    lines.append("### Result Files")
    lines.append("")
    if results_dir.exists():
        for jf in sorted(results_dir.glob("evaluation_results_*.json")):
            lines.append(f"- {jf.name}")
    lines.append("")
    lines.append("---")
    lines.append("*Generated by evaluation.ragas_agents.scripts.generate_report*")
    return "\n".join(lines)


def main() -> int:
    parser = argparse.ArgumentParser(description="Generate evaluation report")
    parser.add_argument("--results-dir", type=Path, default=None, help="Directory with evaluation_results_*.json")
    parser.add_argument("--stats", type=Path, default=None, help="Path to statistical_analysis.json")
    parser.add_argument("--format", nargs="+", default=["md", "json"], help="Output formats: md, json, html")
    parser.add_argument("--output-dir", "-o", type=Path, default=None, help="Output directory for reports")
    parser.add_argument("--config", type=Path, default=None, help="Path to ragas_config.yaml")
    args = parser.parse_args()

    config = load_config(args.config)
    results_dir = args.results_dir
    if results_dir is None:
        results_dir = config.get("paths", {}).get("evaluation_results_dir", REPO_ROOT / "evaluation" / "ragas_agents" / "output" / "evaluation_results")
        results_dir = REPO_ROOT / results_dir if not Path(str(results_dir)).is_absolute() else Path(results_dir)
    results_dir = Path(results_dir).resolve()

    stats_path = args.stats
    if stats_path is None:
        stats_path = results_dir / "statistical_analysis.json"
    else:
        stats_path = Path(stats_path).resolve()

    out_dir = args.output_dir
    if out_dir is None:
        out_dir = config.get("paths", {}).get("reports_dir", REPO_ROOT / "evaluation" / "ragas_agents" / "output" / "reports")
        out_dir = REPO_ROOT / out_dir if not Path(str(out_dir)).is_absolute() else Path(out_dir)
    out_dir = Path(out_dir).resolve()
    out_dir.mkdir(parents=True, exist_ok=True)

    report_md = build_markdown_report(results_dir, stats_path)
    if "md" in args.format:
        md_path = out_dir / "evaluation_report.md"
        md_path.write_text(report_md, encoding="utf-8")
        logger.info("Markdown report written to %s", md_path)

    if "json" in args.format:
        json_path = out_dir / "evaluation_report.json"
        with open(json_path, "w", encoding="utf-8") as f:
            json.dump({"report_md": report_md, "results_dir": str(results_dir), "stats_path": str(stats_path) if stats_path else None}, f, indent=2)
        logger.info("JSON report written to %s", json_path)

    return 0


if __name__ == "__main__":
    sys.exit(main())
